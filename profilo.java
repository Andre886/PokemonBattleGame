    public profilo() {
        initComponents();
        setLocationRelativeTo(null);
        
        
        try (Scanner scan = new Scanner (new File ("profili.txt"))) {
            while(scan.hasNextLine()) {
                String riga = scan.nextLine();
                String[] contenuto = riga.split(" ");
                
               
                
                this.nomev = contenuto[0];
                this.compleannov = contenuto[1];
                this.areav = contenuto[2];
                
                //System.out.println(nomev + compleannov + areav);
                nome.setText(nomev);
                compleanno.setText(compleannov);
                area.setText(areav);
            }
            
            
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(profilo.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }

